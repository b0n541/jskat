package org.jskat.gui.swing;

/**
 * Enumeration for all types of tabs
 */
enum TabType {
    /**
     * Welcome screen
     */
    WELCOME_SCREEN,
    /**
     * Local JSkat table
     */
    JSKAT_TABLE,
    /**
     * ISS login
     */
    ISS_LOGIN,
    /**
     * ISS main screen
     */
    ISS_MAIN_SCREEN,
    /**
     * Skat table on ISS
     */
    ISS_TABLE
}
