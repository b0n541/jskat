package org.jskat.util;

/**
 * Different game variants for skat games
 */
public enum GameVariant {
    /**
     * Standard game with bidding and announcement by the declarer
     */
    STANDARD,
    /**
     * Bock game
     */
    BOCK,
    /**
     * Forced Ramsch game
     */
    FORCED_RAMSCH;
}
