package org.jskat.data;

/**
 * Different types of views in the GUI
 */
public enum JSkatViewType {
    /**
     * Local skat table
     */
    LOCAL_TABLE,
    /**
     * Remote skat table on ISS
     */
    ISS_TABLE,
    /**
     * Training table
     */
    TRAINING_TABLE,
    /**
     * Other views like the welcome panel at start up
     */
    OTHER;
}
